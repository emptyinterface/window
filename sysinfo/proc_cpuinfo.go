func parseCPU(b []byte) CPU {
	var cpu CPU
	fmt.Fscan(bytes.NewReader(b),
		&cpu.Id,
		&cpu.User,
		&cpu.Nice,
		&cpu.System,
		&cpu.Idle,
		&cpu.IOWait,
		&cpu.IRQ,
		&cpu.SoftIRQ,
		&cpu.Steal,
		&cpu.Guest,
		&cpu.GuestNice,
	)
	return cpu
}
